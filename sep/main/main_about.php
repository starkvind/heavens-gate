	<p>Heaven's Gate tiene un objetivo: unificar toda la informaci&oacute;n y el trasfondo que se crea en la partida de <b><a href="http://es.wikipedia.org/wiki/Werewolf:_The_Apocalypse" target="_blank">Hombre Lobo</a></b>: El Apocalipsis, de la que soy <b><a href="http://es.wikipedia.org/wiki/Director_de_juego" target="_blank">Narrador</a></b>.
	<br/>
	Sobra decir que todo lo que se encuentra escrito en la web que sigue a esta presentaci&oacute;n es <b>absolutamente</b> ficticio.</p>
	
	<p>El universo en el que se desarrolla la partida está ambientado en el catastr&oacute;fico <b><a href="http://es.wikipedia.org/wiki/Mundo_de_Tinieblas" target="_blank">Mundo de Tinieblas</a></b>, donde diversos seres sobrenaturales (vampiros, lic&aacute;ntropos, magos, fatas, espectros, etc.) conviven e intentan asegurar su existencia, ya sea ocult&aacute;ndose y haci&eacute;ndose pasar por mortales comunes y corrientes o utilizando sus poderes especiales para destruir a sus enemigos.</p>
	
	<p>A diferencia de otros juegos de rol, en &eacute;ste prima la narraci&oacute;n, y es un punto que me gusta bastante. Las sesiones de juego que realizamos suceden casi sin dar uso a los dados, excepto cuando llegamos a puntos en los que se considera obligatorio (como combates).</p>
	
	<p>Si despu&eacute;s de haber ojeado algo de la p&aacute;gina est&aacute;s teniendo espasmos por cosas como: "¡El viejo Mundo de Tinieblas acaba en 2003! ¡No se puede arbitrar m&aacute;s all&aacute; de ese a&ntilde;o!", o: "¡Los Garou no pueden formar manada con otros cambiantes, es antinatural a no ser que sean Hengeyokai!", t&oacute;mate un momento y lee, por favor.
	<br/>
	La llegada del nuevo <b>Mundo de Tinieblas</b> creado por <b>White Wolf</b> no nos agradó demasiado tanto a los jugadores como a mi, as&iacute; que decidimos continuar con el sistema y el trasfondo antiguo (el de los libros b&aacute;sicos).
	<br/>
	Soy completamente consciente de que no me he leído ni una pizca de la metatrama del <b>Mundo de Tinieblas</b> y que probablemente nuestra partida no se asemeje a la idea que tenían los creadores del juego o la que puedan tener los jugones más exigentes. Pero nosotros nos <b>divertimos</b>, que es lo m&aacute;s importante.
	<br/>
	Si eres un fan exigente del antiguo <b>Mundo de Tinieblas</b>, quiz&aacute;s lo que leas aqu&iacute; haga que tus retinas exploten. As&iacute; que te recordar&eacute; las normas b&aacute;sicas del rol:</p>

	<ul>
		<li>No hay normas.</li>
		<li>Lo importante es divertirse.</li>
		<li>La edición que se juega aquí es la versión "Cojones del Máster".</li>
	</ul>

	<p>Despu&eacute;s de haber le&iacute;do este peque&ntilde;o p&aacute;rrafo... ¡gracias por visitarnos! Si te gusta lo que has visto, no dudes en ponerte en contacto con nosotros.</p>

	<p>Todas las im&aacute;genes y textos pertenecen a sus respectivos autores.
	<br/>
	Todas las situaciones, personas y hechos que aparecen aqu&iacute; retratados son <b>ficticios</b> y cualquier coincidencia con la realidad es mera casualidad. No se recomienda la lectura a personas que no sepan distinguir la realidad de la ficción.</p>
