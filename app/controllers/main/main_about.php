	<h2>Acerca de Heaven's Gate</h2>
	<hr class="main-about-sep"/>
	
	<p><b>Heaven's Gate</b> es el archivo vivo de una cr&oacute;nica de <b><a href="http://es.wikipedia.org/wiki/Werewolf:_The_Apocalypse" target="_blank">Hombre Lobo: El Apocalipsis</a></b> iniciada en 2006. Esta web nace con un prop&oacute;sito claro: reunir, ordenar y preservar todo el trasfondo, personajes y acontecimientos surgidos durante a&ntilde;os de juego.</p>
	<p>El escenario parte del catastr&oacute;fico <b><a href="http://es.wikipedia.org/wiki/Mundo_de_Tinieblas" target="_blank">Mundo de Tinieblas</a></b>, donde vampiros, garou, magos, changelings y otras criaturas sobrenaturales luchan por sobrevivir en una realidad al borde del colapso. Sin embargo, esta cr&oacute;nica no pretende reproducir de forma estricta la metatrama oficial, sino explorar sus posibilidades narrativas desde una continuidad propia.</p>

	<p>Heaven's Gate es una reinterpretaci&oacute;n personal del antiguo Mundo de Tinieblas. A lo largo de los a&ntilde;os ha desarrollado coherencia interna, evoluci&oacute;n de personajes y una l&iacute;nea temporal alternativa que se ha consolidado temporada tras temporada.</p>

	<p>En nuestras sesiones prima la narraci&oacute;n sobre la mec&aacute;nica. Los dados intervienen cuando es necesario, pero el foco siempre est&aacute; en los personajes, sus decisiones y las consecuencias de sus actos. La historia se construye a partir de sus conflictos, sus p&eacute;rdidas y sus elecciones morales.</p>

	<p>Esta web no busca validar ni cuestionar ninguna edici&oacute;n oficial. Tampoco pretende erigirse como versi&oacute;n alternativa &laquo;correcta&raquo;. Es, simplemente, nuestra cr&oacute;nica: un mundo moldeado por a&ntilde;os de juego, debate y evoluci&oacute;n creativa.</p>

	<p>Si eres lector habitual del antiguo Mundo de Tinieblas, es posible que encuentres divergencias respecto a la continuidad publicada. Son deliberadas y forman parte de una identidad propia que se ha forjado con el tiempo.</p>

	<p>Al final, el principio rector es sencillo: el rol es un espacio de creaci&oacute;n compartida. Las reglas sirven a la historia, no al contrario.</p>

	<p>Gracias por visitar este rinc&oacute;n del Apocalipsis.</p>

